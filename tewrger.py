def split_routes(route_text):
    # 使用分隔符“路線一：”、“路線二：”、“路線三：”将文本分成不同的路线
    routes = route_text.split("路線")[1:]

    # 初始化一个空列表以存储每个路线的内容
    route_contents = []

    # 遍历每个路线并分别存储内容
    for i, route in enumerate(routes, start=1):
        # 找到当前路线的名称，使用路線{i}作为标签
        route_name = f"路線{i}："

        # 将路线名称和内容一起存储到列表中
        route_contents.append(f"{route_name}\n{route.strip()}")

    return route_contents


